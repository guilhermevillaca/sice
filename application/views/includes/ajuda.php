<div id="consultar" class="reveal-modal medium">

	<h3><small>Consultar</small></h3>

	<p>
		
		Nessa tela é onde você fará a consulta de todos os livros do seu acervo, e a partir dessa tela pode ser realizada a 
		reserva do livro.

	</p>

	<p>
		
		Modos de Consultas: <br>
		A Consulta deve ser feita pelo nome do livro ou direto por código do Livro, o nome pode ser consultado por partes do
		 titulo como no inicio, no meio ou no final do titulo conforme modelo. <br>
		Ex: Uma consulta com a palavra “ana” pode retornar: <br>
		*Analise de requisitos; <br>
		*Fundamentos da analise de requisitos; <br>
		*Fundamentação da língua Americana

	</p>

