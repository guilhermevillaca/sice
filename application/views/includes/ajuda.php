<div id="emprestimo" class="reveal-modal medium">

	<h3><small>Empréstimo</small></h3>

<p>

	Nessa tela Realiza o empréstimo do livro, que obrigatoriamente vem de uma reserva. 
	Ao acessar o listar reserva e clicar em empréstimo, o sistema abrirá uma nova tela com os seguintes atributos:
	
</p>

<p>
	
	Cod. Livro - Vem por default o livro registrado na reserva, não podendo ser alterado. <br>
	Nome do Livro - Nome do livro que será emprestado, não podendo ser alterado. <br>
	Data de emprestimo - Será adicionado a data de empréstimo do livro. <br>
	Data de devolução - Será setado com 7 dias a mais que a data de empréstimo, mas podendo ser alterada de acordo com a necessidade. <br>
	Nome do requerente - Será setado automaticamente com o nome do requerente, de acordo com a reserva.

</p>

