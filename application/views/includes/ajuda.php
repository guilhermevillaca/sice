<div id="reserva" class="reveal-modal medium">

	<h3><small>Reserva</small></h3>

	<p>
		
		Essa tela será disponibilizada quando ao acessar o consulta e no livro clicar no botão reserva. Ao abrir essa tela você 
		preencherá os campos a seguir:
	</p>

	<p>
		Atributos: <br>
		Data da reserva – Aqui você colocará a data que pretende pegar o Livro. <br>
		Data de devolução – Esse campo será setado por default uma semana corrida a partir da data de reserva, mas poderá ser 
		alterada para casos especiais que saiam do padrão de uma semana. <br>
		Requerente – Aqui deverá ser informado o cód. da pessoa que deseja emprestar o livro, caso seja um aluno ou professor 
		logado ele setará por padrão com o cód. logado. <br>

		Botão: <br>
		Enviar – será confirmada a reserva do livro.

	</p>

